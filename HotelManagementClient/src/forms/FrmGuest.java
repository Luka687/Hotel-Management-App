    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        try {
            int c = JOptionPane.showConfirmDialog(rootPane, "Are you sure you want to delete the guest?");
            if (c == JOptionPane.YES_OPTION){
                Response r = Controller.getInstance().GenericOperation(g, Operation.DELETE_GUEST);
                if (r.getException() == null){
                    JOptionPane.showMessageDialog(rootPane, r.getMessage());
                    this.setVisible(false);
                    this.dispose();
                    new FrmTable(TableType.GUEST_TABLE, u).setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(rootPane, "Could not delete guest!");
                    throw new Exception(r.getException());
                }
                
            }            
        } catch (IOException ex) {
            Logger.getLogger(FrmGuest.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(FrmGuest.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

