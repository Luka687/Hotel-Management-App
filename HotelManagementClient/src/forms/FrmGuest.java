    private void setUpForm() {
        txt_id.setText(g.getId()+"");
        txt_age.setText(g.getAge()+"");
        txt_firstname.setText(g.getFirstname());
        txt_lastname.setText(g.getLastname());
        txt_mail.setText(g.getEmail());
        txt_phone.setText(g.getPhone_number());
        
        txt_id.setEnabled(false);
        
        if(this.fm == FormMode.ADD_MODE){
            txt_id.setText("");
            txt_age.setText("");
            txt_age.setEnabled(true);
            txt_mail.setEnabled(true);
            txt_phone.setEnabled(true);
            txt_firstname.setEnabled(true);
            txt_lastname.setEnabled(true);
            btn_save.setEnabled(false);
            btn_add.setEnabled(true);
            btn_delete.setEnabled(false);
            btn_cancel.setEnabled(true);
        }
        
        if(this.fm == FormMode.VIEW_MODE){
            txt_age.setEnabled(false);
            txt_mail.setEnabled(false);
            txt_phone.setEnabled(false);
            txt_firstname.setEnabled(false);
            txt_lastname.setEnabled(false);
            btn_save.setEnabled(false);
            btn_add.setEnabled(false);
            btn_delete.setEnabled(false);
            btn_cancel.setEnabled(false);
            btn_enable.setEnabled(true);
        }
        
        else if(this.fm == FormMode.EDIT_MODE){
            txt_age.setEnabled(true);
            txt_mail.setEnabled(true);
            txt_phone.setEnabled(true);
            txt_firstname.setEnabled(true);
            txt_lastname.setEnabled(true);
            btn_save.setEnabled(true);
            btn_add.setEnabled(false);
            btn_delete.setEnabled(true);
            btn_cancel.setEnabled(true);
            btn_enable.setEnabled(false);
        }
    }
