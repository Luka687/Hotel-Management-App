    private void setUpForm() {
        txt_id.setText(r.getId()+"");
        txt_floor.setText(r.getFloor()+"");
        String [] items = {"Occupied", "Not Occupied"};
        box_occupied.setModel(new DefaultComboBoxModel(items));
        if (r.isOccupied()){
            box_occupied.setSelectedIndex(0);
        }
        else{
            box_occupied.setSelectedIndex(1);
        }
        
        txt_id.setEnabled(false);
        txt_floor.setEnabled(false);
        
        if(f == FormMode.EDIT_MODE){
            btn_s.setEnabled(true);
            btn_c.setEnabled(true);
            btn_e.setEnabled(false);
            box_occupied.setEnabled(true);
        }
        
        if(f == FormMode.VIEW_MODE){
            btn_s.setEnabled(false);
            btn_c.setEnabled(false);
            btn_e.setEnabled(true);
            box_occupied.setEnabled(false);
        }
    }
