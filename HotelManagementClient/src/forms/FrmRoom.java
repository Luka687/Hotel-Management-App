    private void btn_sActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sActionPerformed
        try {
            if(box_occupied.getSelectedIndex() == 0){
               r.setOccupied(true); 
            }
            else{
                r.setOccupied(false);
            }
            
            Controller.getInstance().GenericOperation(r, Operation.EDIT_ROOM);
            f = FormMode.VIEW_MODE;
            setUpForm();
        } catch (IOException ex) {
            Logger.getLogger(FrmRoom.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(FrmRoom.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_sActionPerformed

